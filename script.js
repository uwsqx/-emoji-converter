// дҝқеӯҳжёҲгҒҝиҫһжӣёгӮ’иӘӯгҒҝиҫјгӮҖ
const saved = localStorage.getItem("emojiDictionary");

if(saved){
    dictionary = JSON.parse(saved);
}

// гӮ«гӮҝгӮ«гғҠвҶ’гҒІгӮүгҒҢгҒӘ
function katakanaToHiragana(str){

return str.replace(/[гӮЎ-гғ¶]/g,function(s){
return String.fromCharCode(s.charCodeAt(0)-0x60);
});

}

// еӨүжҸӣ
function convertText(){

let text=document.getElementById("inputText").value;

text=katakanaToHiragana(text);

let result="";

for(let ch of text){

if(dictionary[ch]){
result+=dictionary[ch];
}else{
result+=ch;
}

}

document.getElementById("output").innerText=result;

}

